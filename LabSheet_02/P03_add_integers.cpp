#include <iostream>
using namespace std;

int main() {
    // Declare integer variables and initialize them
    int number1 = 50;
    int number2 = 75;
    int sum_result;

    // Calculate the sum
    sum_result = number1 + number2;

    // Display the result
    cout << "The sum of " << number1 << " and " << number2 << " is: " << sum_result << endl;
    return 0;
}